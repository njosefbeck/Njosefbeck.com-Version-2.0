					<a name="Kripalu"></a><h2>Kripalu Yoga</h2>
					
					<p>My training has been in  <a href="http://www.kripalu.org">Kripalu 					Yoga</a>, which is a style of yoga that is based primarily off of the 					teachings of Swami Kripalu, <a 					href="http://en.wikipedia.org/wiki/Hatha_yoga">Hatha Yoga</a> and the <a 					href="http://en.wikipedia.org/wiki/Yoga_Sutras_of_Patanjali">Eight-limbed 					Yoga Path</a> as compiled by yogi Patanjali. Kripalu Yoga looks at 					developing the whole person through not only moral and ethical practices 					(the Yamas and the Niyamas) but also through nonjudgmental, passionate 					self-awareness. For myself, yoga is more than just the practice of 					postures (asana), but a way of living in this world with compassion for 					myself and for others.</p>
					
					<p>Kripalu Yoga is an integrated system of self-transformation that 					empowers you to realize your full potential. The journey begins as you 					revitalize the body through postures, breathing exercises, and relaxation 					techniques. It deepens as you learn to focus awareness, enabling you to 					enter meditative states that awaken compassion and clear the mind. 					Through the practice of Kripalu Yoga, you discover how to nurture the 					roots of health, catalyze personal growth, live with greater skillfulness 					and joy, and transform your sense of what it means to be alive.</p>
					
